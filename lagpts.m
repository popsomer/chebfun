% Evaluate Laguerre polynomial via recurrence
function [L, Lp] = eval_Lag(x, n)
Lm2 = 0; 
Lm1 = exp(-x/2); 
Lpm2 = 0; 
Lpm1 = 0;
for k = 0:n-1
    L = ( (2*k+1-x).*Lm1 - k*Lm2 ) / (k + 1);
    Lp = ( (2*k+1-x).*Lpm1 - Lm1 - k*Lpm2 ) / (k + 1);
    Lm2 = Lm1; 
    Lm1 = L;
    Lpm2 = Lpm1; 
    Lpm1 = Lp;
end
end

