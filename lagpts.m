% Compute the expansion of the orthonormal polynomial near zero without e^(x/2)
function p = pl(np, y, alpha, UQ)
z = y/4/np;
pb = -1i*(pi/2 + sqrt(z).*sqrt(1 - z) - acos(sqrt(z) ) ); % = sqrtphitn

% RL = [1, 0] + [1, 0]*sum(RLkf(z,1:T-1,n)./n.^repmat(reshape(1:T-1,[1,1,T-1]),[2,2,1]),3);
RL = [1, 0];
if ( ~isempty(UQ) )
    mx = size(UQ,3) - 1;
    RL = RL + [1, 0]*sum(RLkf(z, np, UQ)./np.^repmat(reshape(1:mx, [1,1,mx]), [2,2,1]), 3);
end

U = (-1)^n*sqrt(1i*n*pb)/z^(1/4)/(1-z)^(1/4)*z^(-alpha/2)*[1, 0]*RL(n,z,T)*...
    [sin( (alpha + 1)/2*acos(2*z - 1) - pi*alpha/2), cos( (alpha + 1)/2*...
    acos(2*z - 1) - pi*s.alpha/2) ; -1i*sin( (alpha - 1)/2*acos(2*z - 1) - ...
    pi*alpha/2), -1i*cos( (alpha - 1)/2*acos(2*z - 1) - pi*alpha/2)]*...
    [2^(-alpha)*besselj(alpha,2i*n*pb); ...
    2^alpha*(besselj(alpha-1,2i*n*pb) -alpha/(2i*n*pb)*besselj(alpha, 2i*n*pb) )];

p = gammaDen(n,T)*s.betan(n,T)^(-s.alpha/2)*s.Ud(T,n,bz/s.betan(n,T));

p = (4*np)^(-1/2-alpha/2)*exp(-np*(1+2*log(2)))*sqrt(2)*2^alpha...
    /sqrt(1-4i*sum((Ul0(1,2,1:(T-1),1) + Ur0(1,2,1:(T-1),1))./(n+1) ...
    .^reshape(1:(T-1),[1,1,T-1]) ) )*U;
end

